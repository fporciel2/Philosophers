#include "philo.h"

int	main(int argc, char **argv)
{
	t_input	input;

	if ((argc < 5) || (argc > 6)
		|| (!philo_take_numbers(&input, argv, argc))
		|| (!philo_take_times(&input, argv)))
		return (0);
	input.argc = (argc - 1);
	return (philo_init(&input));
}
